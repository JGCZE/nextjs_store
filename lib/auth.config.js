export const authConfig = {
  pages: {
    signIn: "/login",
  },
  providers: [],
  callbacks: {
    async jwt({ token, user }) {
      if (user) {
        token.id = user.id;
        token.isAdmin = user.isAdmin;
      }

      return token;
    },
    async session({ session, token }) {
      if (token) {
        session.user.id = token.id;
        session.user.isAdmin = token.isAdmin;
      }
      return session;
    },
    authorized({ auth, request }) {
      const user = auth?.user;
      const isOnCMS = request.nextUrl?.pathname?.startsWith("/cms");

      //Only admin can reach CMS dash board
      if (isOnCMS && !user?.isAdmin) {
        return false;
      }

      return true;
    },
  },
};
